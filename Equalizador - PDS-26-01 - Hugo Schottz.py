class ScrollableControls(ttk.Frame):
    """Painel vertical com barra de rolagem para os controles da interface."""

    def __init__(self, parent: tk.Misc, width: int = 320) -> None:
        super().__init__(parent)

        self.canvas = tk.Canvas(
            self,
            width=width,
            highlightthickness=0,
            borderwidth=0,
        )
        self.scrollbar = ttk.Scrollbar(
            self,
            orient=tk.VERTICAL,
            command=self.canvas.yview,
        )
        self.content = ttk.Frame(self.canvas, padding=(0, 0, 12, 0))

        self.content_window = self.canvas.create_window(
            (0, 0),
            window=self.content,
            anchor=tk.NW,
        )

        self.canvas.configure(yscrollcommand=self.scrollbar.set)

        self.canvas.pack(side=tk.LEFT, fill=tk.BOTH, expand=True)
        self.scrollbar.pack(side=tk.RIGHT, fill=tk.Y)

        self.content.bind("<Configure>", self._update_scroll_region)
        self.canvas.bind("<Configure>", self._update_content_width)

    def _update_scroll_region(self, _event=None) -> None:
        """Atualiza a região que pode ser percorrida pela barra de rolagem."""
        self.canvas.configure(scrollregion=self.canvas.bbox("all"))

    def _update_content_width(self, event) -> None:
        """Mantém a largura dos controles igual à largura visível do painel."""
        self.canvas.itemconfigure(self.content_window, width=event.width)

