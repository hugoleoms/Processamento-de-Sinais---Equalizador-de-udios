def ideal_lowpass(cutoff_hz: float, fs: int, n: np.ndarray) -> np.ndarray:
    """Resposta ao impulso ideal de um passa-baixa centrado em n = 0.

    Para wc = 2*pi*fc/fs:
        h_i[n] = sin(wc*n)/(pi*n), n != 0
        h_i[0] = wc/pi
    """
    if fs <= 0:
        raise ValueError("A frequência de amostragem deve ser maior que zero.")

    if not 0.0 < cutoff_hz < fs / 2:
        raise ValueError("A frequência de corte deve estar entre 0 e fs/2.")

    wc = 2.0 * np.pi * cutoff_hz / fs

    h = np.empty_like(n, dtype=np.float64)

    at_zero = n == 0
    h[at_zero] = wc / np.pi

    nz = ~at_zero
    h[nz] = np.sin(wc * n[nz]) / (np.pi * n[nz])

    return h


def design_equalizer_filters(
    fs: int = FS,
    num_taps: int = NUM_TAPS,
) -> tuple[np.ndarray, np.ndarray]:
    """Cria os cinco FIR de fase linear, com janela de Hamming.

    A construção é complementar:
      h0 = LP(215 Hz)
      h1 = LP(665)  - LP(215)
      h2 = LP(2150) - LP(665)
      h3 = LP(6650) - LP(2150)
      h4 = delta - LP(6650)

    Por consequência, sum_i h_i[n] = delta[n-K].
    """
    if num_taps < 101 or num_taps % 2 == 0:
        raise ValueError("num_taps deve ser ímpar e maior ou igual a 101.")

    k = num_taps // 2
    n = np.arange(-k, k + 1, dtype=np.float64)

    # Janela de Hamming aplicada aos filtros FIR.
    window = np.hamming(num_taps)

    def windowed_lp(fc_hz: float) -> np.ndarray:
        """Cria um passa-baixas ideal e aplica a janela de Hamming."""
        return ideal_lowpass(fc_hz, fs, n) * window

    # Passa-baixa utilizados para criar as cinco bandas complementares.
    lp_215 = windowed_lp(CUTOFFS_HZ[0])
    lp_665 = windowed_lp(CUTOFFS_HZ[1])
    lp_2150 = windowed_lp(CUTOFFS_HZ[2])
    lp_6650 = windowed_lp(CUTOFFS_HZ[3])

    # Impulso unitário deslocado para a posição central do filtro FIR.
    delta = np.zeros(num_taps, dtype=np.float64)
    delta[k] = 1.0

    filters = np.vstack(
        (
            lp_215,
            lp_665 - lp_215,
            lp_2150 - lp_665,
            lp_6650 - lp_2150,
            delta - lp_6650,
        )
    )

    # Verificação numérica da propriedade complementar explicada acima.
    if not np.allclose(filters.sum(axis=0), delta, rtol=0.0, atol=1e-12):
        raise RuntimeError("Os filtros deixaram de formar uma partição complementar.")

    return filters, n.astype(int)


def db_to_linear(db: np.ndarray | float) -> np.ndarray | float:
    """Converte ganho em dB para ganho de amplitude linear."""
    gain = np.power(10.0, np.asarray(db, dtype=np.float64) / 20.0)

    if np.ndim(db) == 0:
        return float(gain)

    return gain


def magnitude_db(values: np.ndarray, floor_db: float = MIN_DB) -> np.ndarray:
    """Magnitude em dB, limitada inferiormente para facilitar visualização."""
    floor_linear = 10.0 ** (floor_db / 20.0)
    return 20.0 * np.log10(np.maximum(np.abs(values), floor_linear))


def resample_to_44k1(audio: np.ndarray, original_fs: int) -> np.ndarray:
    """Reamostra o áudio para 44,1 kHz, quando necessário."""
    if original_fs <= 0:
        raise ValueError("A frequência de amostragem original deve ser maior que zero.")

    if original_fs == FS:
        return audio

    ratio = Fraction(FS, original_fs).limit_denominator()

    return signal.resample_poly(
        audio,
        ratio.numerator,
        ratio.denominator,
        axis=0,
    )


def filter_audio_into_bands(audio: np.ndarray, filters: np.ndarray) -> np.ndarray:
    """Filtra um áudio estéreo/mono por cada FIR.

    Retorna um vetor com formato:
        [5, amostras + num_taps - 1, canais]

    A convolução é calculada por FFT antes da reprodução. Dessa forma, durante
    o play os sliders alteram somente a soma ponderada das cinco bandas, o que
    permite resposta imediata mesmo com FIRs de 2001 coeficientes.
    """
    if audio.ndim != 2:
        raise ValueError("audio deve ter forma [amostras, canais].")

    filtered = []

    for h in filters:
        # O modo "full" mantém o atraso K e a cauda do FIR.
        # Assim, a resposta é compatível com a realização causal do filtro.
        y = signal.fftconvolve(audio, h[:, None], mode="full", axes=0)

        filtered.append(y.astype(np.float32, copy=False))

    return np.stack(filtered, axis=0)


